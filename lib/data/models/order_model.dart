import 'package:virtual_catalog_app/data/models/cart_item_model.dart';
import 'package:virtual_catalog_app/domain/entities/cart_item.dart';
import 'package:virtual_catalog_app/domain/entities/order.dart';

class OrderModel extends Order {
  OrderModel({
    super.id,
    required super.businessId,
    required super.customerName,
    required super.customerLastName,
    required super.customerPhone,
    required super.customerDni,
    super.customerCountry,
    required super.customerAddress,
    required super.customerDepartamento,
    required super.customerProvincia,
    required super.customerDistrito,
    super.customerUbigeoCode,
    super.customerZip,
    super.notes,
    super.customerEmail,
    required super.items,
    required super.total,
    required super.status,
    required super.paymentMethod,
    super.deliveryMethod,
    required super.createdAt,
    super.isBillingSameAsShipping = true,
    super.billingName,
    super.billingLastName,
    super.billingCompany,
    super.billingCountry,
    super.billingAddress,
    super.billingReference,
    super.billingDepartamento,
    super.billingProvincia,
    super.billingDistrito,
    super.billingZip,
    super.billingPhone,
  });

  Map<String, dynamic> toJson() {
    return {
      'businessId': businessId,
      'customerName': customerName,
      'customerLastName': customerLastName,
      'customerPhone': customerPhone,
      'customerDni': customerDni,
      if (customerCountry != null) 'customerCountry': customerCountry,
      'customerAddress': customerAddress,
      'customerDepartamento': customerDepartamento,
      'customerProvincia': customerProvincia,
      'customerDistrito': customerDistrito,
      if (customerUbigeoCode != null) 'customerUbigeoCode': customerUbigeoCode,
      'customerZip': customerZip,
      'notes': notes,
      'customerEmail': customerEmail,
      'items': items.map((x) => CartItemModel.fromEntity(x).toJson()).toList(),
      'total': total,
      'status': status,
      'paymentMethod': paymentMethod,
      'deliveryMethod': deliveryMethod,
      'createdAt': createdAt.toIso8601String(),
      'isBillingSameAsShipping': isBillingSameAsShipping,
      if (billingName != null) 'billingName': billingName,
      if (billingLastName != null) 'billingLastName': billingLastName,
      if (billingCompany != null) 'billingCompany': billingCompany,
      if (billingCountry != null) 'billingCountry': billingCountry,
      if (billingAddress != null) 'billingAddress': billingAddress,
      if (billingReference != null) 'billingReference': billingReference,
      if (billingDepartamento != null) 'billingDepartamento': billingDepartamento,
      if (billingProvincia != null) 'billingProvincia': billingProvincia,
      if (billingDistrito != null) 'billingDistrito': billingDistrito,
      if (billingZip != null) 'billingZip': billingZip,
      if (billingPhone != null) 'billingPhone': billingPhone,
    };
  }

  factory OrderModel.fromJson(Map<String, dynamic> json, String id) {
    return OrderModel(
      id: id,
      businessId: json['businessId'] ?? '',
      customerName: json['customerName'] ?? '',
      customerLastName: json['customerLastName'] ?? '',
      customerPhone: json['customerPhone'] ?? '',
      customerDni: json['customerDni'] ?? '',
      customerCountry: json['customerCountry'],
      customerAddress: json['customerAddress'] ?? '',
      customerDepartamento: json['customerDepartamento'] ?? json['customerRegion'] ?? '',
      customerProvincia: json['customerProvincia'] ?? json['customerRegion'] ?? '',
      customerDistrito: json['customerDistrito'] ?? json['customerCity'] ?? '',
      customerUbigeoCode: json['customerUbigeoCode'],
      customerZip: json['customerZip'],
      notes: json['notes'],
      customerEmail: json['customerEmail'],
      items: List<CartItem>.from(
        json['items']?.map((x) => CartItemModel.fromJson(x).toEntity()) ?? [],
      ),
      total: (json['total'] ?? 0).toDouble(),
      status: json['status'] ?? 'pending',
      paymentMethod: json['paymentMethod'] ?? '',
      deliveryMethod: json['deliveryMethod'],
      createdAt: DateTime.parse(json['createdAt']),
      isBillingSameAsShipping: json['isBillingSameAsShipping'] ?? true,
      billingName: json['billingName'],
      billingLastName: json['billingLastName'],
      billingCompany: json['billingCompany'],
      billingCountry: json['billingCountry'],
      billingAddress: json['billingAddress'],
      billingReference: json['billingReference'],
      billingDepartamento: json['billingDepartamento'] ?? json['billingRegion'],
      billingProvincia: json['billingProvincia'] ?? json['billingRegion'],
      billingDistrito: json['billingDistrito'] ?? json['billingDistrict'],
      billingZip: json['billingZip'],
      billingPhone: json['billingPhone'],
    );
  }

  static OrderModel fromEntity(Order order) {
    return OrderModel(
      id: order.id,
      businessId: order.businessId,
      customerName: order.customerName,
      customerLastName: order.customerLastName,
      customerPhone: order.customerPhone,
      customerDni: order.customerDni,
      customerCountry: order.customerCountry,
      customerAddress: order.customerAddress,
      customerDepartamento: order.customerDepartamento,
      customerProvincia: order.customerProvincia,
      customerDistrito: order.customerDistrito,
      customerUbigeoCode: order.customerUbigeoCode,
      customerZip: order.customerZip,
      notes: order.notes,
      customerEmail: order.customerEmail,
      items: order.items,
      total: order.total,
      status: order.status,
      paymentMethod: order.paymentMethod,
      deliveryMethod: order.deliveryMethod,
      createdAt: order.createdAt,
      isBillingSameAsShipping: order.isBillingSameAsShipping,
      billingName: order.billingName,
      billingLastName: order.billingLastName,
      billingCompany: order.billingCompany,
      billingCountry: order.billingCountry,
      billingAddress: order.billingAddress,
      billingReference: order.billingReference,
      billingDepartamento: order.billingDepartamento,
      billingProvincia: order.billingProvincia,
      billingDistrito: order.billingDistrito,
      billingZip: order.billingZip,
      billingPhone: order.billingPhone,
    );
  }
}
